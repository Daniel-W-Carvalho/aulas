// O NVM (Node Version Manager) é um gerenciador de versões para o Node.js. Ele permite que você instale, 
// gerencie e alterne facilmente entre diferentes versões do Node.js em um sistema. Isso é especialmente útil 
// quando você trabalha em vários projetos que exigem versões específicas do Node.js.



// Principais Características do NVM
// Gerenciamento de Múltiplas Versões:

// Você pode instalar e usar várias versões do Node.js no mesmo sistema.
// Trocar de versão é simples e rápido.
// Fácil Instalação de Versões Específicas:
// Instale qualquer versão do Node.js disponível, incluindo versões mais antigas, LTS (Long Term Support) ou a versão mais recente.
// Isolamento de Versões:
// Cada projeto pode usar uma versão específica do Node.js sem conflitos com outros projetos.
// Compatibilidade com npm:
// Como o npm (Node Package Manager) é instalado junto com o Node.js, ele gerencia automaticamente a versão correta do npm para a versão selecionada do Node.

//COMO USAR ALGUNS DOS COMANDOS

// Instalar uma versão específica do Node.js:
// nvm install 18.17.0

// Usar uma versão específica do Node.js:
// nvm use 18.17.0

// Verificar a versão ativa do Node.js:
// node -v

// Listar versões instaladas:
// nvm list

// Listar todas as versões disponíveis do Node.js:
// nvm ls-remote