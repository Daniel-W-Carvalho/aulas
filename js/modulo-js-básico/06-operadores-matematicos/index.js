let primeiroValor = 10
let segundoValor = 2

console.log(primeiroValor + segundoValor) // Somar valor, se a variavel for string, ele vai concatenar os valores
console.log(primeiroValor - segundoValor) // Subtrair valores
console.log(primeiroValor / segundoValor) // dividir valores
console.log(primeiroValor * segundoValor) // multiplicar valores
console.log(primeiroValor % segundoValor) // resto da divisão

