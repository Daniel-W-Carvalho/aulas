// No JavaScript, existem dois tipos principais de escopo: o escopo global e o escopo local.


// Escopo Global:
// O escopo global é o contexto mais amplo no qual todas as variáveis são acessíveis em um programa.
// Variáveis declaradas fora de qualquer função têm escopo global e podem ser acessadas de qualquer lugar no código.


// Escopo Local:
// O escopo local refere-se ao contexto dentro de uma função onde as variáveis são acessíveis somente dentro dessa função.
// Variáveis declaradas dentro de uma função têm escopo local e não são acessíveis fora dela.
// Isso promove encapsulamento e previne conflitos de nomes entre diferentes partes do código.


// Resumidamente, o escopo global abrange todo o código, enquanto o escopo local está limitado a uma função específica.